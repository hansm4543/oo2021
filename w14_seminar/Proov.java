public class Proov{
    public static void main(String[] args) {

        String name = "nimi"; // string

        char c = 'A'; // All Unicode characters

        byte maxByte = 127; // From +127 to -128

        short maxShort = 32767;  // 	From +32,767 to -32,768

        int maxInt = 2147483647; // From +2,147,483,647 to -2,147,483,648

        long maxLong = 9223372036854775807L;  // From +9,223,372,036,854,775,807 to -9,223,372,036,854,775,808

        double age = 10.5; //From 1.797,693,134,862,315,7 E+308 to 4.9 E-324

        float vanus = 10.5f; // 	From 3.402,823,5 E+38 to 1.4 E-45

        boolean jahei = true; // true false


        System.out.println(name);
        System.out.println(c);
        System.out.println(maxByte);
        System.out.println(maxShort);
        System.out.println(maxInt);
        System.out.println(maxLong);
        System.out.println(age);

        System.out.println(vanus);

        System.out.println(jahei);

    }
}
